import Vector2D = Phaser.Point;

////////////////////////////////////////////////////////////////////////////////
// Alert
////////////////////////////////////////////////////////////////////////////////
export class Alert {
    constructor(coords: Vector2D, game: Phaser.Game) {
	this.coords = coords;
	this.game = game;
    }

    coords: Vector2D;
    game: Phaser.Game;
}


